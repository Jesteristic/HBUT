def extract_task_ids(page_patent_data: Dict[str, Any], page_num: int) -> List[str]:
    """
    从API响应JSON中提取所有taskId。

    Args:
        page_patent_data (dict): API响应JSON

    Returns:
        list: 包含所有taskId的列表
    """
    task_ids = []
    try:
        # 获取专利列表
        patent_list = page_patent_data.get('4', [])

        if not isinstance(patent_list, list):
            logger.error(f"页面 {page_num} 的专利数据格式异常")

        # 遍历专利条目提取taskId
        for idx, patent_item in enumerate(patent_list):
            try:
                if isinstance(patent_item, dict) and '3' in patent_item:
                    task_id = patent_item['3']
                    if isinstance(task_id, str) and task_id.strip():
                        task_ids.append(task_id.strip())
            except Exception as e:
                logger.error(f"处理页面 {page_num} 第 {idx} 个专利条目时出错: {e}")
                continue

    except Exception as e:
        logger.error(f"解析API响应结构时出错: {e}")

    return task_ids


def extract_patent_fields(raw_data: Dict[str, Any]) -> Dict[str, Any]:
    """
    从原始JSON数据中提取专利字段，构建PatentItem对象。

    Args:
        raw_data: 包含专利信息的原始JSON数据

    Returns:
        PatentItem: 结构化后的专利数据对象
    """
    # 获取专利核心数据块
    # 原始数据中，专利信息位于 "1" -> "105" 下
    patent_data = raw_data.get("1", {}).get("105", {})

    # 提取主分类号信息
    main_class_codes_data = raw_data.get("2", [])
    main_class_codes_str = None
    for item in main_class_codes_data:
        if item.get("1") == "mainclasscodeList":
            main_class_codes_str = item.get("2", "")
            break

    # 清理和提取发明人列表
    inventors_raw = patent_data.get("5", [])
    if isinstance(inventors_raw, str):
        inventors = [inventors_raw.strip()] if inventors_raw.strip() else []
    elif isinstance(inventors_raw, list):
        inventors = [inv.strip() for inv in inventors_raw if inv and isinstance(inv, str)]
    else:
        inventors = []

    # 清理和提取IPC分类号
    ipc_classes_raw = patent_data.get("9", [])
    if isinstance(ipc_classes_raw, str):
        ipc_classes = [ipc_classes_raw.strip()] if ipc_classes_raw.strip() else []
    elif isinstance(ipc_classes_raw, list):
        ipc_classes = [ipc.strip() for ipc in ipc_classes_raw
                       if ipc and isinstance(ipc, str) and ipc.strip()]
    else:
        ipc_classes = []

    # 解析日期
    def parse_date(date_str: Optional[str]) -> Optional[date]:
        if not date_str or date_str == "0000-00-00 00:00:00":
            return None
        try:
            return datetime.strptime(date_str.split()[0], "%Y-%m-%d").date()
        except (ValueError, AttributeError):
            return None

    # 计算原始数据哈希
    raw_json_str = json.dumps(raw_data, sort_keys=True, ensure_ascii=False)
    raw_data_hash = hashlib.md5(raw_json_str.encode('utf-8')).hexdigest()

    # 构建并返回PatentItem对象
    return {
        # 核心元数据
        "patent_internal_id": patent_data.get("1", ""),  # 内部ID
        "application_number": patent_data.get("3", ""),  # 申请号
        "publication_number": patent_data.get("4"),  # 公开号
        "title": patent_data.get("2", ""),  # 标题
        "inventors": inventors,  # 发明人列表
        "applicant": patent_data.get("7"),  # 申请人
        "applicant_address": patent_data.get("19"),  # 申请人地址
        "agency": patent_data.get("20"),  # 代理机构
        "agent": patent_data.get("21"),  # 代理人
        "abstract_text": patent_data.get("12", ""),  # 摘要
        "claims_text": patent_data.get("22", ""),  # 权利要求
        "description_text": None,  # 原始数据中没有完整说明书
        "patent_type": patent_data.get("13"),  # 专利类型
        "ipc_classes": ipc_classes,  # IPC分类号列表
        "main_ipc": patent_data.get("10"),  # 主IPC
        "application_date": parse_date(patent_data.get("15")),  # 申请日
        "publication_date": parse_date(patent_data.get("16")),  # 公开日
        "country_code": patent_data.get("17"),  # 国别代码
        "language": patent_data.get("24"),  # 语言
        "legal_status": patent_data.get("46"),  # 法律状态
        "patent_link": patent_data.get("25"),  # 专利链接

        # 技术分类信息
        "main_class_codes": main_class_codes_str,  # 主分类号路径

        # 系统元数据
        "source_db": patent_data.get("14", "WF"),  # 数据来源
        "raw_data_hash": raw_data_hash  # 原始数据哈希
    }


